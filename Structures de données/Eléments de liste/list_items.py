# Crée une nouvelle liste
animals = ["elephant", "lion", "tiger", "giraffe", "monkey", "dog"]
print(animals)

# Remplace deux éléments : "lion" et "tiger" par un élément : "cat"
animals[1:3] = ["cat"]
print(animals)

# Supprime deux éléments : "cat" et "giraffe" de la liste
animals[1:3] = []
print(animals)

